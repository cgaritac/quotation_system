    public void cargarDatos() {

        //Crea un modelo para la tabla
        this.modeloTabla = new DefaultTableModel() {

            //Método para impedir que la la información de la tabla sea editable de forma directa por el usuario
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //Regresa el valor de la variable
            }
        };

        //Se establecen los nombres de los encabezados de las columnas de la tabla
        modeloTabla.addColumn("N° Cotización");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Cédula");

        //Se define un objeto para manejar la informacion a ingresar en cada fila
        Object[] fila;
        this.tbCotizacionesNuevos.setModel(modeloTabla); //Se establece el modelo de la tabla 
        
//Ciclo para recorrer un objeto
        for (DatosPersonales d : vehiculosNuevos) {

            //Se define un array de 3 campos y se asigna a la variable "fila"
            fila = new Object[3];

            //Se asignan los valores de las variables de instancia del objeto a cada campo del array "fila"
            fila[0] = d.getNumeroCotizacion();
            fila[1] = d.getNombre();
            fila[2] = d.getIdentificacion();

            modeloTabla.addRow(fila); //Se adicionan los valores del array "fila" a una fila del modelo de la tabla           
        }

        //Crea un modelo para la tabla
        this.modeloTabla2 = new DefaultTableModel() {

            //Método para impedir que la la información de la tabla sea editable de forma directa por el usuario 
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //Regresa el valor de la variable
            }
        };

        //Se establecen los nombres de los encabezados de las columnas de la tabla
        modeloTabla2.addColumn("N° Cotización");
        modeloTabla2.addColumn("Nombre");
        modeloTabla2.addColumn("Cédula");

        this.tbCotizacionesUsados.setModel(modeloTabla2); //Se establece el modelo de la tabla 
        //Ciclo para recorrer un objeto 
        for (DatosPersonales d : vehiculosUsados) {

            //Se define un array de 3 campos y se asigna a la variable "fila"
            fila = new Object[3];

            //Se asignan los valores de las variables de instancia del objeto p a cada campo del array "fila"
            fila[0] = d.getNumeroCotizacion();
            fila[1] = d.getNombre();
            fila[2] = d.getIdentificacion();

            modeloTabla2.addRow(fila); //Se adicionan los valores del array "fila" a una fila del modelo de la tabla           
        }
    }

