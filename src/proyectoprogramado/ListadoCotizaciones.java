    private void btnBuscarCotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCotActionPerformed
        if(this.rdbVNuevo.isSelected()) {
            //Obtiene un valor númerico en caso de que la fila se encuentre seleccionada y lo asigna a una variable local "fila"
            int fila = this.tbCotizacionesNuevos.getSelectedRow();

            //Condicional para determinar si la fila se encuentra seleccionada para continuar con el formulario de edición de datos
            if (fila != -1) {

                //Obtiene el valor del número de cotización en la fila seleccionada de la tabla y lo almacena en una variable local llamada "id"
                int id = (int) this.tbCotizacionesNuevos.getValueAt(fila, 0);

                String tipo = "Nuevo";

                //Crea el objeto "ventana" de la clase "Mensaje" y lo inicializa mediante el método constructor "Mensaje"
                Mensaje ventana = new Mensaje(new javax.swing.JDialog(), true, tipo, id);

                //Hace visible el objeto ventana
                ventana.setVisible(true);

            } else {
                //Muestra mensaje en pantalla 
                JOptionPane.showMessageDialog(this,
                        "Debe seleccionar el número de cotización para continuar",
                        "Alto",
                        JOptionPane.WARNING_MESSAGE);
            }

        } else if (this.rdbVUsado.isSelected()) {
            //Obtiene un valor númerico en caso de que la fila se encuentre seleccionada y lo asigna a una variable local "fila"
            int fila = this.tbCotizacionesUsados.getSelectedRow();

            //Condicional para determinar si la fila se encuentra seleccionada para continuar con el formulario de edición de datos
            if (fila != -1) {

                //Obtiene el valor del número de cotización en la fila seleccionada de la tabla y lo almacena en una variable local llamada "id"
                int id = (int) this.tbCotizacionesUsados.getValueAt(fila, 0);

                String tipo = "Usado";

                //Crea el objeto "ventana" de la clase "Mensaje" y lo inicializa mediante el método constructor "Mensaje"
                Mensaje ventana = new Mensaje(new javax.swing.JDialog(), true, tipo, id);

                //Hace visible el objeto ventana
                ventana.setVisible(true);

            } else {
                //Muestra mensaje en pantalla 
                JOptionPane.showMessageDialog(this,
                        "Debe seleccionar el número de cotización para continuar",
                        "Alto",
                        JOptionPane.WARNING_MESSAGE);
            }

        } else {
            //Muestra mensaje en pantalla 
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar un tipo de cotización y el número de cotización para continuar",
                    "Alto",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarCotActionPerformed


